// ── Cron calculation from timezone ───────────────────────────
// Daily fetch should run at midnight in the user's timezone.
// GitHub Actions cron is always UTC, so we calculate the offset.

export const midnightCronUTC = (timezone: string): string => {
  // Use Jan 1 (standard time) to avoid DST-dependent offset.
  // During DST the cron fires ~1h late (e.g. 01:00 local), which is safe
  // because daily-fetch resolves "yesterday" from the local date.
  // The opposite (firing 1h early = 23:00 the previous day) would shift
  // the local date backward and collect the wrong day.
  const jan1 = new Date(new Date().getFullYear(), 0, 1, 0, 0, 0);
  const utcMidnight = new Date(
    jan1.toLocaleString("en-US", { timeZone: "UTC" }),
  );
  const localMidnight = new Date(
    jan1.toLocaleString("en-US", { timeZone: timezone }),
  );
  const offsetMinutes = Math.round(
    (utcMidnight.getTime() - localMidnight.getTime()) / 60000,
  );
  const utcHour = ((offsetMinutes / 60 + 24) % 24) | 0;
  const utcMinute = ((offsetMinutes % 60) + 60) % 60;
  return `${utcMinute} ${utcHour} * * *`;
};

// ── Workflow YAML template ───────────────────────────────────

export type WorkflowOpts = {
  username: string;
  language: Language;
  timezone: string;
  siteTitle: string;
  theme: Theme;
  llmProvider?: LLMProvider;
  llmModel?: string;
  llmSecretName?: string;
};

export const buildDailyWorkflow = (opts: WorkflowOpts): string => {
  const cron = midnightCronUTC(opts.timezone);

  return `# Generated by: github-weekly-reporter setup
# Docs: https://github.com/deariary/github-weekly-reporter

name: Daily Fetch

on:
  schedule:
    - cron: '${cron}'  # midnight ${opts.timezone}
  workflow_dispatch:

permissions:
  contents: write

jobs:
  daily:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - uses: deariary/github-weekly-reporter@main
        with:
          github-token: \${{ secrets.GH_PAT }}
          username: '${opts.username}'
          mode: 'daily'
          language: '${opts.language}'
          timezone: '${opts.timezone}'
`;
};

export const weeklyCronUTC = (timezone: string): string => {
  // Weekly report runs 1 hour after daily fetch, on the local Monday.
  // Because cron is in UTC, the UTC day-of-week may differ from the
  // local day-of-week for timezones with positive offsets (e.g.
  // Asia/Tokyo local Monday 01:00 = UTC Sunday 16:00, day-of-week 0).
  const dailyCron = midnightCronUTC(timezone);
  const [minute, hour] = dailyCron.split(" ").map(Number);
  const weeklyHour = (hour + 1) % 24;
  // Compute tz offset: positive = local is ahead of UTC (e.g. +540 for Asia/Tokyo).
  const jan1 = new Date(new Date().getFullYear(), 0, 1, 0, 0, 0);
  const utcMidnight = new Date(
    jan1.toLocaleString("en-US", { timeZone: "UTC" }),
  );
  const localMidnight = new Date(
    jan1.toLocaleString("en-US", { timeZone: timezone }),
  );
  const offsetMinutes = Math.round(
    (localMidnight.getTime() - utcMidnight.getTime()) / 60000,
  );
  // Local Monday 01:00 is 60 minutes after Monday 00:00 local.
  // In UTC that is (60 - offsetMinutes) minutes from Monday 00:00 UTC.
  const utcMinutesFromMondayStart = 60 - offsetMinutes;
  const dayShift = Math.floor(utcMinutesFromMondayStart / (24 * 60));
  const utcDow = (((1 + dayShift) % 7) + 7) % 7;
  return `${minute} ${weeklyHour} * * ${utcDow}`;
};

export const buildWeeklyWorkflow = (opts: WorkflowOpts): string => {
  const weeklyCron = weeklyCronUTC(opts.timezone);

  const llmInputs =
    opts.llmProvider && opts.llmModel && opts.llmSecretName
      ? `          llm-provider: '${opts.llmProvider}'
          llm-model: '${opts.llmModel}'
          ${LLM_API_KEY_INPUT_NAMES[opts.llmProvider]}: \${{ secrets.${opts.llmSecretName} }}`
      : "";

  return `# Generated by: github-weekly-reporter setup
# Docs: https://github.com/deariary/github-weekly-reporter

name: Weekly Report

on:
  schedule:
    - cron: '${weeklyCron}'  # Monday, 1 hour after daily fetch (${opts.timezone})
  workflow_dispatch:
    inputs:
      date:
        description: 'Target date (YYYY-MM-DD, default: today)'
        required: false
        type: string

permissions:
  contents: write
  pages: write

env:
  SITE_TITLE: '${opts.siteTitle}'

jobs:
  weekly:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - uses: deariary/github-weekly-reporter@main
        with:
          github-token: \${{ secrets.GH_PAT }}
          username: '${opts.username}'
          mode: 'weekly'
          language: '${opts.language}'
          timezone: '${opts.timezone}'
          theme: '${opts.theme}'
          date: \${{ inputs.date }}
${llmInputs}
`;
};

// ── README template ──────────────────────────────────────────

